from django.test import TestCase

from core.games.ConnectFour import ConnectFour


class ConnectFourTest(TestCase):
    def test_is_board_valid(self):
        self.assertTupleEqual(
            ConnectFour.is_board_valid(
                [
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                ]
            ),
            (True, None),
        )

        self.assertTupleEqual(
            ConnectFour.is_board_valid(
                [
                    ["*", "*", "*", "X", "*", "O", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                ]
            ),
            (True, None),
        )

        self.assertTupleEqual(
            ConnectFour.is_board_valid(
                [
                    ["*", "*", "X", "X", "X", "X", "*"],
                    ["*", "*", "O", "O", "O", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                ]
            ),
            (True, None),
        )

        self.assertFalse(
            ConnectFour.is_board_valid(
                [
                    ["*", "*", "X", "D", "X", "X", "*"],
                    ["*", "*", "O", "O", "O", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                ]
            )[0]
        )

        self.assertFalse(
            ConnectFour.is_board_valid(
                [
                    ["*", "X", "X", "*", "X", "X", "*"],
                    ["*", "*", "*", "O", "O", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                ]
            )[0]
        )

        self.assertFalse(
            ConnectFour.is_board_valid(
                [
                    ["*", "O", "X", "*", "X", "X", "*"],
                    ["*", "*", "*", "O", "O", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "X", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                    ["*", "*", "*", "*", "*", "*", "*"],
                ]
            )[0]
        )

